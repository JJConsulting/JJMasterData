    {
        return Icons.Select(i => new DataItemValue(i.Id.ToString(), i.Description, i.Icon, "#000000"));
    }

    public static string GetClassName(this IconType icon)
    {
        return GetIconInfo(icon).ClassName;
    }

    public static IconInfo GetIconInfo(this IconType icon)
    {
        return Icons.First(x => x.Icon.Equals(icon));
    }
